CREATE TABLE species (
  id INT GENERATED BY DEFAULT AS IDENTITY,
  name text,
  scientific_name text,
  number_in_wild int, 
  conservation_code text,
  created_at timestamp without time zone DEFAULT now()
  PRIMARY KEY(id)
);

