CREATE TABLE individuals (
 id INT GENERATED BY DEFAULT AS IDENTITY,
 name text,
 nickname text,
 created_at timestamp without time zone DEFAULT now(),
 species_id int,
 PRIMARY KEY(id),
 FOREIGN KEY (species_id) REFERENCES species(id)
);

