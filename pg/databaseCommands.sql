CREATE TABLE sightings (
    id INT GENERATED BY DEFAULT AS IDENTITY,
   date_of_sighting date,
   location varchar(255),
   healty boolean,
    email_of_sighter text,
   individual_id int,
    PRIMARY KEY (id),
    FOREIGN KEY (individual_id) REFERENCES individuals(id)
);

